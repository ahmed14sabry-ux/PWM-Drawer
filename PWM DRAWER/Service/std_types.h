typedef unsigned short      			u16 ;
typedef unsigned int        			u32 ;
typedef unsigned long long int         	u64 ;
typedef signed char      				s8 ;
typedef signed short      				s16 ;
typedef signed int        				s32 ;
typedef signed long long int         	s64 ;
typedef float                             f32;
typedef double                            f64;

#define NULL                            (void *)(0)
#endif
